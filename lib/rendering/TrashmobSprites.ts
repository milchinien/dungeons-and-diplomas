/**
 * Trashmob sprite renderer
 */
export class TrashmobSpriteRenderer {
  private animationTime: number = 0;

  /**
   * Update animation timer
   */
  update(dt: number): void {
    this.animationTime += dt;
  }

  /**
   * Draw a trashmob sprite
   */
  draw(
    ctx: CanvasRenderingContext2D,
    type: TrashmobType,
    x: number,
    y: number,
    size: number,
    animation: TrashmobAnimationType,
    direction: Direction,
    frameOverride?: number
  ): void {
    const frames = SPRITE_FRAMES[type];
    const palette = PALETTES[type];
    const speeds = ANIMATION_SPEEDS[type];

    // Select animation frames
    const animFrames = frames[animation];
    const speed = speeds[animation];

    // Calculate current frame
    const frameIndex = frameOverride !== undefined
      ? Math.min(frameOverride, animFrames.length - 1)
      : Math.floor(this.animationTime * speed) % animFrames.length;
    const frame = animFrames[frameIndex];

    // Calculate pixel size (sprite is 12x12)
    const pixelSize = size / 12;

    // Flip horizontally if facing left
    const flipX = direction === 'left';

    ctx.save();

    if (flipX) {
      ctx.translate(x + size, y);
      ctx.scale(-1, 1);
      x = 0;
      y = 0;
    }

    // Draw each pixel
    for (let py = 0; py < 12; py++) {
      for (let px = 0; px < 12; px++) {
        const colorIndex = frame[py][px];
        if (colorIndex === 0) continue; // Transparent

        const color = palette[colorIndex];
        if (color === 'transparent') continue;

        ctx.fillStyle = color;
        ctx.fillRect(
          Math.floor(x + px * pixelSize),
          Math.floor(y + py * pixelSize),
          Math.ceil(pixelSize),
          Math.ceil(pixelSize)
        );
      }
    }

    ctx.restore();
  }

  /**
   * Get the number of frames for an animation
   */
  getFrameCount(type: TrashmobType, animation: TrashmobAnimationType): number {
    return SPRITE_FRAMES[type][animation].length;
  }

  /**
   * Get current animation time (for syncing multiple sprites)
   */
  getAnimationTime(): number {
    return this.animationTime;
  }

  /**
   * Set animation time (for individual sprite timing)
   */
  setAnimationTime(time: number): void {
    this.animationTime = time;
  }

  /**
   * Reset animation time
   */
  resetAnimationTime(): void {
    this.animationTime = 0;
  }
}

// Singleton instance for shared animation timing
export const trashmobSpriteRenderer = new TrashmobSpriteRenderer();
